public class Point {
    private final int x;
    private final int y;

    public Point() {
        x = 0;
        y = 0;
    }

    public Point(int x_, int y_) {
        x = x_;
        y = y_;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }


}
